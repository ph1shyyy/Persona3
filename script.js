const menuList = document.getElementById("menuList");
const heroRank = document.getElementById("heroRank");
const heroEyebrow = document.getElementById("heroEyebrow");
const heroTitle = document.getElementById("heroTitle");
const heroCopy = document.getElementById("heroCopy");
const statGrid = document.getElementById("statGrid");
const quickFacts = document.getElementById("quickFacts");
const detailKicker = document.getElementById("detailKicker");
const detailTitle = document.getElementById("detailTitle");
const detailIndex = document.getElementById("detailIndex");
const detailSummary = document.getElementById("detailSummary");
const detailTags = document.getElementById("detailTags");
const detailList = document.getElementById("detailList");
const startOverlay = document.getElementById("startOverlay");
const startButton = document.getElementById("startButton");

let activeIndex = 0;

function buildMenu() {
  menuList.innerHTML = "";

  sections.forEach((section, index) => {
    const button = document.createElement("button");
    button.type = "button";
    button.className = "menu-item";
    button.dataset.index = String(index);
    button.innerHTML = `
      <span class="menu-item-index">${section.index}</span>
      <span class="menu-item-copy">
        <strong>${section.label}</strong>
        <small>${section.subtitle}</small>
      </span>
      <span class="menu-item-rank">${section.rank}</span>
    `;

    button.addEventListener("click", () => setActiveSection(index));
    button.addEventListener("mouseenter", () => setActiveSection(index));
    menuList.appendChild(button);
  });
}

function renderStats(items) {
  statGrid.innerHTML = "";

  items.forEach((item) => {
    const pill = document.createElement("div");
    pill.className = "stat-pill";
    pill.innerHTML = `<strong>${item.value}</strong><span>${item.label}</span>`;
    statGrid.appendChild(pill);
  });
}

function renderQuickFacts(items) {
  quickFacts.innerHTML = "";

  items.forEach((item) => {
    const li = document.createElement("li");
    li.textContent = item;
    quickFacts.appendChild(li);
  });
}

function renderEntries(entries) {
  detailList.innerHTML = "";

  entries.forEach((entry) => {
    const wrapper = document.createElement("article");
    wrapper.className = "detail-entry";

    const metaHtml = (entry.meta || []).map((value) => `<span>${value}</span>`).join("");
    const bodyHtml = entry.body ? `<p>${entry.body}</p>` : "";
    const bulletsHtml = entry.bullets
      ? `<ul>${entry.bullets.map((bullet) => `<li>${bullet}</li>`).join("")}</ul>`
      : "";

    wrapper.innerHTML = `
      <div class="detail-entry-header">
        <h3>${entry.title}</h3>
        <div class="detail-entry-meta">${metaHtml}</div>
      </div>
      ${bodyHtml}
      ${bulletsHtml}
    `;

    detailList.appendChild(wrapper);
  });
}

function setActiveSection(index) {
  activeIndex = (index + sections.length) % sections.length;
  const section = sections[activeIndex];

  document.querySelectorAll(".menu-item").forEach((item, itemIndex) => {
    item.classList.toggle("is-active", itemIndex === activeIndex);
    item.setAttribute("aria-current", itemIndex === activeIndex ? "page" : "false");
  });

  heroRank.textContent = section.heroRank;
  heroEyebrow.textContent = section.subtitle;
  heroTitle.textContent = section.heroTitle;
  heroCopy.textContent = section.heroCopy;

  detailKicker.textContent = section.subtitle;
  detailTitle.textContent = section.label;
  detailIndex.textContent = section.index;
  detailSummary.textContent = section.summary;
  detailTags.innerHTML = section.tags.map((tag) => `<span>${tag}</span>`).join("");

  renderStats(section.stats);
  renderQuickFacts(section.quickFacts);
  renderEntries(section.entries);
}

function hideOverlay() {
  startOverlay.classList.add("is-hidden");
}

function showOverlay() {
  startOverlay.classList.remove("is-hidden");
}

document.addEventListener("keydown", (event) => {
  const overlayVisible = !startOverlay.classList.contains("is-hidden");

  if (overlayVisible && (event.key === "Enter" || event.key === " ")) {
    event.preventDefault();
    hideOverlay();
    return;
  }

  if (event.key === "Escape") {
    showOverlay();
    return;
  }

  if (overlayVisible) {
    return;
  }

  if (event.key === "ArrowUp") {
    event.preventDefault();
    setActiveSection(activeIndex - 1);
  }

  if (event.key === "ArrowDown") {
    event.preventDefault();
    setActiveSection(activeIndex + 1);
  }
});

startButton.addEventListener("click", hideOverlay);
startOverlay.addEventListener("click", (event) => {
  if (event.target === startOverlay) {
    hideOverlay();
  }
});

buildMenu();
setActiveSection(0);
